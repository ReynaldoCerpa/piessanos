package Model;

import java.util.Date;

public class Promocion {
    private String codigo, nombre, clave_tratamiento, fecha, descuento;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getClave_tratamiento() {
        return clave_tratamiento;
    }

    public void setClave_tratamiento(String clave_tratamiento) {
        this.clave_tratamiento = clave_tratamiento;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getDescuento() {
        return descuento;
    }

    public void setDescuento(String descuento) {
        this.descuento = descuento;
    }






}
